package week1.day1;

public class Maths {
	//To add the numbers
	public  int add(int num1, int num2)
	{ 
		return num1+num2;
	}
	//To multiply the numbers
	public  int multiply(int num1, int num2)
	{ 
		return num1*num2;
	}

	//To divide the numbers
	public  int divide(int num1, int num2) { 
		return num1/num2;
	}

}
